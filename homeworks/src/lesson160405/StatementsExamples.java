package lesson160405;

public class StatementsExamples {

	public static void main(String[] args) {
		
		int x; // variable declaration = объявление переменной
		
		int y = 10; //variable declaration with initialization = объявление переменной с присваиванием
		
		x = 2; // assignment statement = оператор присваивания 
		
		y = x / 20 + 15;
		
		/*
		 * LOAD X
		 * ADD 1
		 * STOR X
		 */
		
		x++;
	}

}
